    /**
     * Spot a marker on your Blackfire Timeline.
     * This needs 2 envVars: BLACKFIRE_SERVER_ID and BLACKFIRE_SERVER_TOKEN
     * @param Feed $feed
     * @return void
     * @throws \Symfony\Contracts\HttpClient\Exception\TransportExceptionInterface
     */
    public function addBlackfireMarker(Feed|Marker $object): int
    {
        $response = $this->client->request(
            'POST',
            'https://blackfire.io/api/v1/events',
            [
                'headers' => [
                    'Accept' => 'application/json',
                    'Content-type' => 'application/json',
                    'Authorization' => 'Basic ' . base64_encode($this->blackfireServerId . ':' . $this->blackfireServerToken),
                ],
                'json' => ['name' => substr($object->getBlackfireMarkerTitle(), 0, 64), 'timestamp' => $object->getPublished()->getTimestamp()]
            ]
        );
        
        return $response->getStatusCode();
    }

    /**
     * Function to sanitize a string and remove all UTF-8 characters
     *  - remove HTML tags
     *  - decode HTML special chars
     *  - replace accents
     *  - return only the 64 first characters
     * @param string $title
     * @return string
     */
